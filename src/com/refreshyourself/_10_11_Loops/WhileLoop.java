package com.refreshyourself._10_11_Loops;

public class WhileLoop {
    public static void main(String[] args) {

        int apples = 1;

        while(apples <= 10){

            System.out.println("I am eating my " + apples +". apple :)");
            apples++;

        }

        System.out.println("I am full now.. :X");
    }
}
